parallelogram_height = 6
parallelogram_area = area_of_parallelogram(base, parallelogram_height)
print(f"Length of base: {base}")
print(f"Height of parallelogram: {parallelogram_height}")
print(f"Expected Output: {parallelogram_area:.1f}")
